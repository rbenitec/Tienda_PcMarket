package entities;

public class Categoria {
    private int catid;
    private String catnom;
    private int catord;

    public int getCatid() {
        return catid;
    }

    public void setCatid(int catid) {
        this.catid = catid;
    }

    public String getCatnom() {
        return catnom;
    }

    public void setCatnom(String catnom) {
        this.catnom = catnom;
    }

    public int getCatord() {
        return catord;
    }

    public void setCatord(int catord) {
        this.catord = catord;
    }
}
